"""The personal layer against a real Postgres (issue #16).

Slice 11 records the owner-specific layer — Goals, Markers, Decisions — through the
`personal` service and links it to the impersonal evidence layer by Concept
overlap. This drives those repository reads and `personal` writes — the same code
the HTTP API wraps — over a real Postgres + pgvector, starting from a genuinely
admitted Candidate (the Slice-8 path) so the Concepts a Decision overlaps with are
the ones extraction actually built. Concept mentions are resolved through the same
`FakeEmbedder`/`ConceptNormalizer` the admit pipeline uses, so a personal-layer
mention merges onto the canonical Concept the Body of Knowledge already minted.

Covers, per the acceptance criteria: Goal/Marker/Decision CRUD; a Marker being
append-only with derived out-of-range and a viewable series; a Decision carrying
its own actual parameters distinct from the Protocol it implements; "adopt a
Protocol"; and suggest-then-confirm links generated by Concept overlap and
individually confirmable.
"""

from __future__ import annotations

from datetime import datetime, timezone

import psycopg
import pytest

from health_bok import personal, review
from health_bok.repository import Repository
from tests.fakes import FakeEmbedder, FakeExtractor
from tests.seed import seed_processed_video
from tests.test_admission import (
    EMBED_MODEL,
    RAPAMYCIN_CLAIM,
    drain_daily,
    make_extraction,
    normalizer,
)

VIDEO_ID = "vid_personal"
AT = datetime(2026, 3, 1, 8, 0, tzinfo=timezone.utc)


def _admit_bok(repo: Repository) -> None:
    """Admit a video so the BoK has Concepts/Claims/Protocols to overlap against."""
    seed_processed_video(repo, video_id=VIDEO_ID)
    review.approve_candidate(VIDEO_ID, repo=repo)
    drain_daily(FakeExtractor(make_extraction()), repo)


def _concepts_by_name(repo: Repository) -> dict:
    return {c.name: c for c in repo.list_concepts()}


# -- Goals ------------------------------------------------------------------


def test_goal_records_with_concepts_and_shows_serving_decisions(conn):
    repo = Repository(conn)
    _admit_bok(repo)

    # Record a Goal concerning a Concept the BoK already knows ("rapamycin"); the
    # mention normalizes onto that same canonical Concept, not a duplicate.
    goal_id = personal.record_goal(
        title="Lower cardiovascular risk",
        detail="Keep apoB and inflammation low.",
        concepts=["rapamycin"],
        normalizer=normalizer(repo),
        repo=repo,
    )

    goals = repo.list_goals()
    assert [g.title for g in goals] == ["Lower cardiovascular risk"]
    goal = goals[0]
    assert [c.name for c in goal.concepts] == ["rapamycin"]
    assert goal.concepts[0].id == _concepts_by_name(repo)["rapamycin"].id  # merged
    assert goal.served_by == []  # an unmet Goal — nothing serves it yet

    # A Decision that serves the Goal makes it visible on the Goal (AC: "see which
    # Decisions serve a Goal").
    decision_id = personal.record_decision(
        action="Take rapamycin", dose="6mg", timing=None, frequency="weekly",
        duration=None, started_at=AT, ended_at=None, note=None,
        concepts=["rapamycin"], implements_protocol_id=None,
        normalizer=normalizer(repo), repo=repo,
    )
    assert personal.link_decision(
        decision_id, target_type="goal", target_id=goal_id, repo=repo
    ) is True

    reread = repo.get_goal(goal_id)
    assert [d.action for d in reread.served_by] == ["Take rapamycin"]

    # Deleting the serving Decision returns the Goal to unmet, no edge left dangling.
    assert personal.delete_decision(decision_id, repo=repo) is True
    assert repo.get_goal(goal_id).served_by == []


def test_delete_goal_removes_it_and_its_edges(conn):
    repo = Repository(conn)
    _admit_bok(repo)
    goal_id = personal.record_goal(
        title="Improve sleep", detail=None, concepts=["sleep"],
        normalizer=normalizer(repo), repo=repo,
    )
    assert _edges_touching(conn, "goal", goal_id) == 1  # its one `references` edge
    assert personal.delete_goal(goal_id, repo=repo) is True
    assert repo.get_goal(goal_id) is None
    assert _edges_touching(conn, "goal", goal_id) == 0
    assert "sleep" in _concepts_by_name(repo)  # the Concept hub is untouched
    assert personal.delete_goal(goal_id, repo=repo) is False  # clean no-op


def test_edit_goal_concepts_attach_detach_and_normalize(conn, postgres_url):
    """A Goal's Concepts are editable after creation (issue #37)."""
    repo = Repository(conn)
    _admit_bok(repo)
    norm = normalizer(repo)

    # Start from a Goal with one Concept; the rest are attached on the detail page.
    goal_id = personal.record_goal(
        title="Lower cardiovascular risk", detail=None, concepts=["rapamycin"],
        normalizer=norm, repo=repo,
    )

    # Attach a Concept the BoK already knows by picking it from the catalogue: the
    # mention normalizes onto that *same* canonical hub, not a near-duplicate.
    existing = _concepts_by_name(repo)
    assert "mitochondrial density" in existing  # minted by the admitted Candidate
    assert personal.attach_goal_concept(
        goal_id, name="mitochondrial density", normalizer=norm, repo=repo
    ) is True
    attached = {c.name: c.id for c in repo.get_goal(goal_id).concepts}
    assert attached == {
        "rapamycin": existing["rapamycin"].id,
        "mitochondrial density": existing["mitochondrial density"].id,  # reused
    }

    # Attach is idempotent — re-adding an attached Concept doesn't duplicate the edge.
    assert personal.attach_goal_concept(
        goal_id, name="mitochondrial density", normalizer=norm, repo=repo
    ) is True
    assert len(repo.get_goal(goal_id).concepts) == 2

    # Typing a term that isn't in the catalogue mints a genuinely new Concept hub.
    before = len(repo.list_concepts())
    assert personal.attach_goal_concept(
        goal_id, name="VO2 max", normalizer=norm, repo=repo
    ) is True
    assert len(repo.list_concepts()) == before + 1
    assert "VO2 max" in {c.name for c in repo.get_goal(goal_id).concepts}

    # The edits survive a reload (they are persisted `references` edges) — proven
    # over a *fresh* connection, not just a re-read of the writer's own.
    with psycopg.connect(postgres_url) as other:
        assert {c.name for c in Repository(other).get_goal(goal_id).concepts} == {
            "rapamycin", "mitochondrial density", "VO2 max",
        }

    # Detach each Concept; removing the last one leaves the Goal valid (empty set ok).
    for c in list(repo.get_goal(goal_id).concepts):
        assert personal.detach_goal_concept(
            goal_id, concept_id=c.id, repo=repo
        ) is True
    assert repo.get_goal(goal_id).concepts == []
    assert _edges_touching(conn, "goal", goal_id) == 0
    # Detaching an absent Concept is a clean no-op; the Concept hubs are untouched.
    assert personal.detach_goal_concept(
        goal_id, concept_id=existing["rapamycin"].id, repo=repo
    ) is False
    assert "rapamycin" in _concepts_by_name(repo)


def test_suggest_existing_concepts_for_a_goal(conn):
    """Suggest existing Concepts a Goal likely concerns, for one-click attach (#38).

    The deterministic suggest half of suggest-then-confirm: the Goal's text is
    embedded and matched against the existing Concept embeddings over pgvector, so
    every suggestion already exists (no minting, no LLM) and an attached Concept is
    never re-suggested. The Goal-text vectors are placed at chosen cosine distances
    from the BoK Concepts, the same controlled-vector trick normalization tests use.
    """
    repo = Repository(conn)
    _admit_bok(repo)  # mints the BoK Concepts (incl. rapamycin, creatine monohydrate)
    norm = normalizer(repo)

    goal_id = personal.record_goal(
        title="Lower cardiovascular risk", detail=None, concepts=[],
        normalizer=norm, repo=repo,
    )
    # The suggester embeds the Goal's text; the BoK Concept embeddings were minted at
    # admit (CONCEPT_VECTORS). Place this Goal near rapamycin [..1..] and creatine
    # [...1.], orthogonal (and so far) from the other three Concepts.
    embedder = FakeEmbedder({
        "Lower cardiovascular risk": [0, 0, 1, 1, 0],
        "Train for a marathon": [0, 0, 0, 0, 0, 1],  # orthogonal to every Concept
    })

    before = len(repo.list_concepts())
    suggestions = personal.suggest_goal_concepts(
        goal_id, embedder=embedder, repo=repo, model=EMBED_MODEL
    )
    # Every suggestion resolves to an *existing* Concept and the catalogue did not
    # grow — this path never mints a Concept or calls the LLM.
    existing = _concepts_by_name(repo)
    assert len(repo.list_concepts()) == before
    assert {s.name for s in suggestions} == {"rapamycin", "creatine monohydrate"}
    assert all(s.concept_id == existing[s.name].id for s in suggestions)

    # Already-attached Concepts are never suggested: confirm one (the #37 attach) and
    # it drops out of the next pass, leaving the other.
    assert personal.attach_goal_concept(
        goal_id, name="rapamycin", normalizer=norm, repo=repo
    ) is True
    after = personal.suggest_goal_concepts(
        goal_id, embedder=embedder, repo=repo, model=EMBED_MODEL
    )
    assert {s.name for s in after} == {"creatine monohydrate"}

    # A Goal whose text matches nothing within the cutoff yields an empty list, not
    # an error — and a missing Goal is a clean empty list too.
    barren = personal.record_goal(
        title="Train for a marathon", detail=None, concepts=[],
        normalizer=norm, repo=repo,
    )
    assert personal.suggest_goal_concepts(
        barren, embedder=embedder, repo=repo, model=EMBED_MODEL
    ) == []
    assert personal.suggest_goal_concepts(
        9999, embedder=embedder, repo=repo, model=EMBED_MODEL
    ) == []


def test_attach_goal_concept_rejects_empty_and_missing_goal(conn):
    """Empty terms and gone Goals are rejected rather than minting junk (issue #37)."""
    repo = Repository(conn)
