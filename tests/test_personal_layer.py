def test_marker_is_append_only_time_series_with_derived_out_of_range(conn):
    repo = Repository(conn)
    norm = normalizer(repo)

    # Three apoB readings over time: high → still high → in range. apoB has a
    # one-sided reference range (only an upper bound).
    r1 = personal.record_marker(
        concept="apoB", value=130, unit="mg/dL", reference_low=None,
        reference_high=90, measured_at=datetime(2026, 1, 1, tzinfo=timezone.utc),
        normalizer=norm, repo=repo,
    )
    personal.record_marker(
        concept="apoB", value=110, unit="mg/dL", reference_low=None,
        reference_high=90, measured_at=datetime(2026, 3, 1, tzinfo=timezone.utc),
        normalizer=norm, repo=repo,
    )
    personal.record_marker(
        concept="apoB", value=85, unit="mg/dL", reference_low=None,
        reference_high=90, measured_at=datetime(2026, 6, 1, tzinfo=timezone.utc),
        normalizer=norm, repo=repo,
    )

    # The series view: one entry per Concept, latest reading + full count.
    series = repo.list_marker_series()
    assert len(series) == 1
    apob = series[0]
    assert apob.concept.name == "apoB"
    assert apob.reading_count == 3
    assert apob.latest.value == 85.0  # newest by measured_at
    assert apob.latest.out_of_range is False  # 85 <= 90 → derived in range

    # The history renders as a series, newest first, out-of-range derived per row.
    history = repo.marker_history(apob.concept.id)
    assert [h.value for h in history] == [85.0, 110.0, 130.0]
    assert [h.out_of_range for h in history] == [False, True, True]

    # A two-sided range derives both directions; a value inside is in range.
    personal.record_marker(
        concept="fasting glucose", value=60, unit="mg/dL", reference_low=70,
        reference_high=99, measured_at=AT, normalizer=norm, repo=repo,
    )
    glucose = {s.concept.name: s for s in repo.list_marker_series()}["fasting glucose"]
    assert glucose.latest.out_of_range is True  # 60 < 70 → below the low bound

    # Append-only: a reading is an immutable dated snapshot, never overwritten.
    with pytest.raises(psycopg.errors.RaiseException):
        with conn.cursor() as cur:
            cur.execute("UPDATE markers SET value = 1 WHERE id = %s", (r1,))
    conn.rollback()
    with pytest.raises(psycopg.errors.RaiseException):
        with conn.cursor() as cur:
            cur.execute("DELETE FROM markers WHERE id = %s", (r1,))
    conn.rollback()
    assert {h.value for h in repo.marker_history(apob.concept.id)} == {85.0, 110.0, 130.0}


