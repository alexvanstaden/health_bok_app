def test_decision_carries_own_parameters_and_adopts_a_protocol(conn):
    repo = Repository(conn)
    _admit_bok(repo)
    protocol = repo.list_protocols()[0]  # "Take creatine monohydrate", dose 5g
    assert protocol.dose == "5g"

    # Adopt the Protocol: the Decision pre-fills from it but records the owner's
    # *own* actual dose (3g, a deliberate deviation) — distinct from the Protocol's.
    decision_id = personal.record_decision(
        action="Take creatine monohydrate", dose="3g", timing="morning",
        frequency="daily", duration=None, started_at=AT, ended_at=None,
        note="Half the recommended dose to start.", concepts=[],
        implements_protocol_id=protocol.id,
        normalizer=normalizer(repo), repo=repo,
    )

    decision = repo.get_decision(decision_id)
    assert decision.dose == "3g"  # the owner's actual parameter, not the Protocol's 5g
    assert [p.action for p in decision.implements] == ["Take creatine monohydrate"]
    # Adopting inherits the Protocol's Concepts, so overlap works immediately.
    assert [c.name for c in decision.concepts] == ["creatine monohydrate"]


def test_suggested_links_are_generated_by_overlap_and_individually_confirmable(conn):
    repo = Repository(conn)
    _admit_bok(repo)
    norm = normalizer(repo)

    # A Goal concerning rapamycin (overlaps the rapamycin Claim and any rapamycin
    # Decision); recorded before the Decision so it can be suggested.
    goal_id = personal.record_goal(
        title="Extend healthspan", detail=None, concepts=["rapamycin"],
        normalizer=norm, repo=repo,
    )

    # A Decision concerning both rapamycin and creatine — so the suggester finds a
    # Protocol (creatine), a Claim (rapamycin), and a Goal (rapamycin) by overlap.
    decision_id = personal.record_decision(
        action="Adopt a longevity stack", dose=None, timing=None, frequency=None,
        duration=None, started_at=AT, ended_at=None, note=None,
        concepts=["rapamycin", "creatine monohydrate"], implements_protocol_id=None,
        normalizer=norm, repo=repo,
    )

    suggestions = personal.suggest_decision_links(decision_id, repo=repo)
    by_type = {s.target_type: s for s in suggestions}
    assert set(by_type) == {"protocol", "claim", "goal"}  # one relevant of each kind
    assert by_type["protocol"].label == "Take creatine monohydrate"
    assert by_type["protocol"].shared_concepts == ["creatine monohydrate"]
    assert by_type["claim"].label == RAPAMYCIN_CLAIM
    assert by_type["claim"].shared_concepts == ["rapamycin"]
    assert by_type["goal"].target_id == goal_id
    assert by_type["goal"].shared_concepts == ["rapamycin"]

    # Confirm just the Protocol — links are individually confirmable, not all-or-nothing.
    assert personal.link_decision(
        decision_id, target_type="protocol",
        target_id=by_type["protocol"].target_id, repo=repo,
    ) is True

    # The confirmed Protocol drops out of the next round; the other two remain.
    remaining = {s.target_type for s in personal.suggest_decision_links(decision_id, repo=repo)}
    assert remaining == {"claim", "goal"}

    # Confirm the Claim (supporting evidence) and the Goal (served).
    assert personal.link_decision(
        decision_id, target_type="claim", target_id=by_type["claim"].target_id, repo=repo
    ) is True
    assert personal.link_decision(
        decision_id, target_type="goal", target_id=goal_id, repo=repo
    ) is True
    assert personal.suggest_decision_links(decision_id, repo=repo) == []  # all linked

    # A Marker reading motivates the Decision (owner-chosen, not suggested).
    reading_id = personal.record_marker(
        concept="rapamycin", value=1, unit="x", reference_low=None,
        reference_high=None, measured_at=AT, normalizer=norm, repo=repo,
    )
    assert personal.link_decision(
        decision_id, target_type="marker", target_id=reading_id, repo=repo
    ) is True

    # The Decision's rationale is reviewable from one place: supporting evidence
    # (Claims), the Goals it serves, the Protocol it implements, the Marker behind it.
    decision = repo.get_decision(decision_id)
    assert [p.action for p in decision.implements] == ["Take creatine monohydrate"]
    assert [c.text for c in decision.supported_by] == [RAPAMYCIN_CLAIM]
    assert [g.title for g in decision.serves] == ["Extend healthspan"]
    assert [m.id for m in decision.motivated_by] == [reading_id]

    # And the Goal now shows the Decision serving it (no longer unmet).
    assert [d.action for d in repo.get_goal(goal_id).served_by] == ["Adopt a longevity stack"]

    # A confirmed link is detachable; re-confirming is idempotent (no duplicate edge).
    assert personal.unlink_decision(
        decision_id, target_type="goal", target_id=goal_id, repo=repo
    ) is True
    assert repo.get_decision(decision_id).serves == []
    assert personal.link_decision(decision_id, target_type="goal", target_id=goal_id, repo=repo)
    assert personal.link_decision(decision_id, target_type="goal", target_id=goal_id, repo=repo)
    assert len(repo.get_decision(decision_id).serves) == 1  # unique constraint deduped


def test_unknown_link_target_is_rejected(conn):
    repo = Repository(conn)
    _admit_bok(repo)
    decision_id = personal.record_decision(
        action="x", dose=None, timing=None, frequency=None, duration=None,
        started_at=AT, ended_at=None, note=None, concepts=[],
        implements_protocol_id=None, normalizer=normalizer(repo), repo=repo,
    )
    with pytest.raises(personal.UnknownLinkTarget):
        personal.link_decision(decision_id, target_type="concept", target_id=1, repo=repo)
    conn.rollback()


def _edges_touching(conn, node_type: str, node_id: int) -> int:
    with conn.cursor() as cur:
        cur.execute(
            "SELECT count(*) FROM edges WHERE (src_type = %s AND src_id = %s) "
            "OR (dst_type = %s AND dst_id = %s)",
            (node_type, node_id, node_type, node_id),
        )
        return cur.fetchone()[0]
